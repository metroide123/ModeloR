package br.com.roleta.modelo;

/**
 *
 * @author Rodolfo Fonseca
 */
public class numerosorteado {
    
    private int idNumeros;
    private String numero;
    private String dataHora;
    private int idUsuario;
    private int idRoleta;

    public int getIdNumeros() {
        return idNumeros;
    }

    public void setIdNumeros(int idNumeros) {
        this.idNumeros = idNumeros;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getDataHora() {
        return dataHora;
    }

    public void setDataHora(String dataHora) {
        this.dataHora = dataHora;
    }

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    public int getIdRoleta() {
        return idRoleta;
    }

    public void setIdRoleta(int idRoleta) {
        this.idRoleta = idRoleta;
    }
    
    
            
            
}
