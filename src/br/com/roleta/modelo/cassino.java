

package br.com.roleta.modelo;

public class cassino {
    
    private int idCassino;
    private String nome;
    private int tempodeSessao;
    private String moeda;

    public int getIdCassino() {
        return idCassino;
    }

    public void setIdCassino(int idCassino) {
        this.idCassino = idCassino;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getTempodeSessao() {
        return tempodeSessao;
    }

    public void setTempodeSessao(int tempodeSessao) {
        this.tempodeSessao = tempodeSessao;
    }

    public String getMoeda() {
        return moeda;
    }

    public void setMoeda(String moeda) {
        this.moeda = moeda;
    }
    
    
}
