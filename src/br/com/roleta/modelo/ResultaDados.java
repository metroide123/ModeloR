
package br.com.roleta.modelo;


public class ResultaDados {
    
    private int id;
    private String entrada;
    private String resultado;
    private String P1;
    private String P2;
    private String P3;
    private int tamanhoespera;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEntrada() {
        return entrada;
    }

    public void setEntrada(String entrada) {
        this.entrada = entrada;
    }

    public String getResultado() {
        return resultado;
    }

    public void setResultado(String resultado) {
        this.resultado = resultado;
    }

    public String getP1() {
        return P1;
    }

    public void setP1(String P1) {
        this.P1 = P1;
    }

    public String getP2() {
        return P2;
    }

    public void setP2(String P2) {
        this.P2 = P2;
    }

    public String getP3() {
        return P3;
    }

    public void setP3(String P3) {
        this.P3 = P3;
    }

    public int getTamanhoespera() {
        return tamanhoespera;
    }

    public void setTamanhoespera(int tamanhoespera) {
        this.tamanhoespera = tamanhoespera;
    }
    
    
    
}
